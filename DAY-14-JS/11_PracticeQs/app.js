// Calculate POPcorn price.

let popCornSize = "S";

if(popCornSize === "XL"){
    console.log(`Popcorn ${popCornSize} size : Rs. 250.`);
}else if(popCornSize === "L"){
    console.log(`Popcorn ${popCornSize} size : Rs. 200.`);
}else if(popCornSize === "M"){
    console.log(`Popcorn ${popCornSize} size : Rs. 100.`);
}else{
    console.log(`Popcorn ${popCornSize} size : Rs. 50.`);
}