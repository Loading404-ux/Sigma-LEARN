// Switch Statement.


let color="red";

switch(color){
    case "red":
        console.log("STOP!!!");
        break;
    case "yellow":
        console.log("GO SLOW!!!");
        break;
    case "green":
        console.log("GO!!");
        break;
    default:
        console.log("Light is Broken!!!");
}

console.log("After switch statement");